        <h3 class="thin center">University Policy on Human Subjects Research</h3>
        
        <div class="row" id="simple">
            <div class="spacer"></div>
            <p class="flow-text">All research involving human subjects, which is to be conducted by faculty, staff or students from Ashesi University College must be approved by the Human Subjects Review Commitee. This committee is the <span class="bold">Institutional Review Board (IRB) </span> of Ashesi University. The purpose of thisreview is to ensure that the research activity is conducted with the safety of the human subject as its highest priority. The criteria for this review are in strict accord with international standards for the protection of human participants in academic research. All research to be conducted by students, faculty, staff, or research affiliates from other institutions must be approved prior to primary data collection or project initiation.</p>

            <p class="flow-text">Human Subjects approval must precede submission of all research applications to any sponsor
			for funding of the research effort. This applies to funding requests for internal or external
			resources, domestic and international applications. This policy applies to all applications
			including those to for-profit, not-for-profit, governmental or charitable organizations.</p>

            <p class="flow-text">The committee will respond to each proposal <span class="bold">within four weeks</span> following its review. If theCommittee determines that a submission involves vulnerable populations or greater than minimal risk, the applicant will have an opportunity meet with the Committee to address issues of
			concern. The applicant will have an opportunity to modify the research design, sampling
			strategies, process or format of data collection, or any other concern that is raised by the
			Committee. Appeals can be formally addressed by the Provost with the Chair of the Human
			Subjects Review Committee. If an appeal is approved, the full committee will revisit the
			proposal and will entertain all concerns of the Principal Investigator. In the event that an appeal
			is also denied, the proposal must be closed for further consideration unless methodological and
			ethical issues are adequately addressed in the form of a new submission.</p>


            
                
            

